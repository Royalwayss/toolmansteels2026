            <div class="col-12 col-lg-7">
               <h3 class="mt-4 product-detail-sub-heading">Heat Treatment and Processing</h3>
               <h4>Hardening</h4>
               <ul class="ml10">
                  <li>Heat to 870°C (1600°F) and hold for 1 hour per inch of thickness.</li>
                  <li>Ensure protective measures to prevent oxidation and decarburization.</li>
                  <li>Cool using oil, air, or high-velocity gas depending on the required hardness and component thickness.</li>
               </ul>
               <h4>Tempering</h4>
               <ul class="ml10">
                  <li>Temper immediately after cooling when the temperature reaches 50–80°C (120–180°F).</li>
                  <li>Maintain one hour per inch of thickness at the tempering temperature.</li>
                  <li>Double tempering is recommended with cooling to room temperature in between.</li>
                  <li>Proper tempering enhances mechanical properties and prevents stress-induced cracks.</li>
               </ul>
               <h4>Continuous Cooling Transformation (CCT) and Tempering Curve:</h4>
               <ul class="ml10">
                  <li>The CCT diagram helps in selecting optimal cooling rates to achieve desired microstructures.</li>
                  <li>Tempering  curves determine mechanical properties at different tempering temperatures.</li>
               </ul>
