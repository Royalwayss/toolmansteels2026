<h3 class="mt-4 product-detail-sub-heading">What is H11 Tool Steel?</h3>
<p>H11 (DIN 1.2343 / X37CrMoV5-1) belongs to the family of&nbsp;<strong>5% chromium hot work tool steels</strong>. It is designed specifically for tooling exposed to repeated heating and cooling cycles, where resistance to thermal cracking and shock becomes critical.</p>
<p>Compared to H13, H11 contains slightly lower vanadium and carbon content, which gives it:</p>
<ul class="ml10">
  <li>Higher toughness</li>
  <li>Better crack resistance</li>
  <li>Improved resistance to thermal shock</li>
</ul>
<p>This makes H11 particularly valuable for tooling that experiences severe cyclic stresses or intermittent water cooling.</p>
